        private readonly IConfiguration _configuration;

        // STORED PROCEDURE
        const string DB_ESQUEMA = "";
        const string SP_GET_LIST_BY_ESTADO = DB_ESQUEMA + "INV_SP_GetListAlmacenByEstado";
        const string SP_GET_LIST_PRODUCCION = DB_ESQUEMA + "INV_SP_GetListAlmacenProduccion";
        const string SP_GET_LIST_BY_FILTRO = DB_ESQUEMA + "INV_SP_GetListAlmacenByFiltro";
        const string SP_GET_LIST_BY_WHSCODE_ITEMCODE = DB_ESQUEMA + "INV_SP_GetListAlmacenByWhsCodeAndItemCode";
        const string SP_GET_BY_CODE = DB_ESQUEMA + "INV_SP_GetAlmacenByCode";

        public AlmacenSapRepository(IConnectionSql context, IConfiguration configuration)
            : base(context)
        {
            _cnxSap = Utilidades.GetExtraerCadenaConexion(configuration, "ParametersConectionSap");
            _configuration = configuration;
            _aplicacionName = GetType().Name;
        }

        public async Task<ResultadoTransaccion<AlmacenSapEntity>> GetListByEstado(AlmacenSapEntity value)
        {
            var response = new List<AlmacenSapEntity>();
            var resultadoTransaccion = new ResultadoTransaccion<AlmacenSapEntity>();

            _metodoName = regex.Match(System.Reflection.MethodBase.GetCurrentMethod().DeclaringType.Name).Groups[1].Value.ToString();

            resultadoTransaccion.NombreMetodo = _metodoName;
            resultadoTransaccion.NombreAplicacion = _aplicacionName;

            try
            {
                using (SqlConnection conn = new SqlConnection(_cnxSap))
                {
                    conn.Open();

                    using (SqlCommand cmd = new SqlCommand(SP_GET_LIST_BY_ESTADO, conn))
                    {
                        cmd.CommandType = CommandType.StoredProcedure;
                        cmd.CommandTimeout = 0;
                        cmd.Parameters.Add(new SqlParameter("@Inactive", value.Inactive));

                        using (var reader = await cmd.ExecuteReaderAsync())
                        {
                            response = (List<AlmacenSapEntity>)context.ConvertTo<AlmacenSapEntity>(reader);
                        }
                    }

                    resultadoTransaccion.IdRegistro = 0;
                    resultadoTransaccion.ResultadoCodigo = 0;
                    resultadoTransaccion.ResultadoDescripcion = string.Format("Registros Totales {0}", response.Count);
                    resultadoTransaccion.dataList = response;
                }
            }
            catch (Exception ex)
            {
                resultadoTransaccion.IdRegistro = -1;
                resultadoTransaccion.ResultadoCodigo = -1;
                resultadoTransaccion.ResultadoDescripcion = ex.Message.ToString();
            }

            return resultadoTransaccion;
        }

